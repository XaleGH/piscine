int	ft_sqrt(int nb)
{
	int	i;
	int	res;

	i = 0;
	res = 0;
	if (nb >= 0)
	{
		while (i <= 46341)
		{
			res = i * i;
			if (res == nb)
				return (i);
			else
				i++;
		}
	}
	return (0);
}
